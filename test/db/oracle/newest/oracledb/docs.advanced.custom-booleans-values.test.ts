// Documentation snippets for the Custom booleans values page
// (docs/advanced/custom-booleans-values.md). Demonstrates
// `CustomBooleanTypeAdapter` — a column-level mapping between JS
// boolean and arbitrary DB values (e.g. 'Y'/'N').
//
// The seed schema does NOT have a custom-boolean column on it (adding
// one would ripple across every test). Instead we declare a local
// `class TFlag extends Table<...>` that exists only as a TypeScript
// description; we never execute against it. The CaptureInterceptor
// records the emitted SQL in mock mode so we can still assert on
// dialect-specific output.

import { afterAll, beforeAll, beforeEach, describe, expect, test } from '../../../../lib/testRunner.js'
import { Table } from '../../../../../src/Table.js'
import { CustomBooleanTypeAdapter } from '../../../../../src/TypeAdapter.js'
import { DBConnection } from '../../domain/connection.js'
import { ctx } from './setup.js'

class TFlag extends Table<DBConnection, 'TFlag'> {
    id     = this.autogeneratedPrimaryKey('id', 'int')
    name   = this.column('name', 'string')
    active = this.column('active', 'boolean', new CustomBooleanTypeAdapter('Y', 'N'))
    constructor() { super('flag_table_only_for_sql_test') }
}
const tFlag = new TFlag()

describe(ctx.label, () => {
    beforeAll(() => ctx.up(), ctx.timeoutMs)
    afterAll(() => ctx.down(), ctx.timeoutMs)
    beforeEach(() => { ctx.reset() })

    test('docs:custom-booleans-values/insert-maps-true-false-to-Y-N', async () => {
        ctx.mockNext(99)

        // doc-start
        let id: number | undefined
        let caught: unknown
        try {
            id = await ctx.conn.insertInto(tFlag).set({
                    name:   'demo',
                    active: true,
                })
                .returningLastInsertedId()
                .executeInsert()
        } catch (e) {
            // expected on real DB — the test-only table doesn't exist.
            caught = e
        }
        // doc-end

        // SQL is captured by the interceptor before the runner executes,
        // so the snapshot is reliable in both modes.
        expect(ctx.lastSql).toMatchInlineSnapshot(`"insert into flag_table_only_for_sql_test (name, active) values (:0, case when (:1 = 1) then 'Y' else 'N' end) returning id into :2"`)
        expect(ctx.lastParams).toMatchInlineSnapshot(`
          [
            "demo",
            1,
            {
              "dir": 3003,
            },
          ]
        `)
        if (ctx.realDbEnabled) {
            // The test-only table doesn't exist on the real DB, so the
            // insert throws (ORA-00942) — the SQL above was still captured.
            expect(caught).toBeInstanceOf(Error)
            expect((caught as Error).message).toContain('does not exist')
        } else {
            expect(id).toBe(99)
        }
    })

    test('docs:custom-booleans-values/select-maps-Y-N-to-true-false', async () => {
        ctx.mockNext([{ id: 1, name: 'demo', active: true }])

        // doc-start
        let rows: Array<{ id: number; name: string; active: boolean }> | undefined
        let caught: unknown
        try {
            rows = await ctx.conn.selectFrom(tFlag)
                .where(tFlag.active)
                .select({
                    id:     tFlag.id,
                    name:   tFlag.name,
                    active: tFlag.active,
                })
                .executeSelectMany()
        } catch (e) {
            // expected on real DB — the test-only table doesn't exist.
            caught = e
        }
        // doc-end

        // tests-audit-disable-next-line mirror-image -- the test-only table doesn't exist on the real DB, so the value path can only be validated under the mock; the real-DB branch asserts the expected ORA-00942 instead
        if (ctx.realDbEnabled) {
            // The test-only table doesn't exist on the real DB (ORA-00942).
            expect(caught).toBeInstanceOf(Error)
            expect((caught as Error).message).toContain('does not exist')
        } else {
            expect(rows).toEqual([{ id: 1, name: 'demo', active: true }])
        }

        expect(ctx.lastSql).toMatchInlineSnapshot(`"select id as "id", name as "name", case when active = 'Y' then 1 else 0 end as "active" from flag_table_only_for_sql_test where (active = 'Y')"`)
        expect(ctx.lastParams).toMatchInlineSnapshot(`[]`)
    })
})
