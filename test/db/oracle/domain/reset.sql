-- Data-only reset for the per-test reseed (onReseed). See the postgres
-- reset.sql header for the rationale. This matters MOST on Oracle: every worker
-- user shares one instance's data dictionary, so the old DROP+CREATE of ~12
-- tables + 3 sequences per reseed serialised all workers on the dictionary's
-- library/row-cache locks. This replaces that with plain DML deletes plus a
-- handful of targeted resets — a large reduction in shared-dictionary DDL.
--
-- Oracle TRUNCATE can't be used freely here (FK-referenced parents, and it does
-- not rewind an identity column), so this deletes children-first, then rewinds
-- only what the seed/tests actually advance:
--   * webhook_event.id is the one IDENTITY the seed advances (it is the only
--     auto-seeded table — every other id is seeded by literal, and Oracle's
--     GENERATED BY DEFAULT does not advance the identity on an explicit insert,
--     so those stay at their start). Rewound to 1 with ALTER TABLE ... MODIFY.
--   * the standalone sequences the sequence tests bump are rewound with
--     ALTER SEQUENCE ... RESTART.
-- Re-running the unchanged seed.sql afterwards reproduces the exact same rows +
-- counters a full schema.sql + seed.sql would. Lists mirror schema.sql; keep
-- them in sync when a table/sequence is added. (`organization` is quoted to
-- match its case-sensitive schema declaration; the rest are unquoted.)
DELETE FROM project_review;
DELETE FROM project_release;
DELETE FROM audit_entry;
DELETE FROM webhook_event;
DELETE FROM calendar_year;
DELETE FROM invoice;
DELETE FROM issue_worklog;
DELETE FROM country;
DELETE FROM issue;
DELETE FROM project;
DELETE FROM app_user;
DELETE FROM "organization";
ALTER TABLE webhook_event MODIFY id GENERATED BY DEFAULT ON NULL AS IDENTITY (START WITH 1);
ALTER SEQUENCE issue_id_seq RESTART START WITH 1;
ALTER SEQUENCE audit_tag_seq RESTART START WITH 1;
ALTER SEQUENCE release_tag_seq RESTART START WITH 1;
