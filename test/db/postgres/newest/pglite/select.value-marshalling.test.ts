// Coverage of AbstractConnection value marshalling
// (`transformValueToDB` / `transformValueFromDB`) for the bigint, double
// and uuid value types, added to the shared `issue` domain as
// `viewCount` (bigint), `estimatedHours` (double) and `externalRef`
// (uuid). Each insert→select round-trip exercises both directions:
//   - INSERT params go through `transformValueToDB` (the JS value reaches
//     the driver),
//   - the SELECT result goes through `transformValueFromDB` (the value
//     comes back and is re-typed to bigint / number / string).
//
// Bodies run inside `ctx.withRollback(...)` so the inserted row is
// reverted on a real DB; in mock mode the queued values stand in. The
// value assertion is identical in both modes: `expected` carries the
// exact JS values inserted, and `transformValueFromDB` maps whatever the
// engine returns back to the same JS value. Selections filter by the
// stable `number` literal (not the autogenerated id) so the param
// snapshot is identical in mock and real mode.
//
// The bigint + uuid cases are grouped because both are unsupported by the
// `sqlite3` connector (no BigInt binding; no uuid_blob/uuid_str extension
// functions for the default uuid strategy), so that test is commented out
// in that one cell — see test/FUTURE_CONNECTORS.md. The double case has
// no such limitation and stays live everywhere.

import { afterAll, beforeAll, beforeEach, describe, expect, test } from '../../../../lib/testRunner.js'
import { assertType, type Exact } from '../../../../lib/assertType.js'
import { tIssue } from '../../domain/connection.js'
import { ctx } from './setup.js'

describe(ctx.label, () => {
    beforeAll(() => ctx.up(), ctx.timeoutMs)
    afterAll(() => ctx.down(), ctx.timeoutMs)
    beforeEach(() => { ctx.reset() })

    test('marshalling/bigint-and-uuid-insert-select-roundtrip', async () => {
        const ref = '0a8f9c1e-1111-4222-8333-444455556666'

        await ctx.withRollback(async () => {
            ctx.mockNext(501) // mocked new id; real DB assigns its own
            const newId = await ctx.conn.insertInto(tIssue)
                .values({
                    projectId:   1,
                    number:      9002,
                    title:       'Bigint and uuid',
                    status:      'open',
                    priority:    2,
                    viewCount:   1500n,
                    externalRef: ref,
                })
                .returningLastInsertedId()
                .executeInsert()

            expect(ctx.lastSql).toMatchInlineSnapshot(`"insert into issue (project_id, number, title, status, priority, view_count, external_ref) values ($1, $2, $3, $4, $5, $6, $7) returning id"`)
            expect(ctx.lastParams).toMatchInlineSnapshot(`
              [
                1,
                9002,
                "Bigint and uuid",
                "open",
                2,
                1500n,
                "0a8f9c1e-1111-4222-8333-444455556666",
              ]
            `)

            const expected = { id: newId, views: 1500n, ref }
            ctx.mockNext(expected)
            const row = await ctx.conn.selectFrom(tIssue)
                .where(tIssue.number.equals(9002))
                .select({
                    id:    tIssue.id,
                    views: tIssue.viewCount,
                    ref:   tIssue.externalRef,
                })
                .executeSelectOne()

            expect(ctx.lastSql).toMatchInlineSnapshot(`"select id as id, view_count as views, external_ref as ref from issue where number = $1"`)
            expect(ctx.lastParams).toMatchInlineSnapshot(`
              [
                9002,
              ]
            `)
            assertType<Exact<typeof row, { id: number; views: bigint; ref?: string }>>()
            expect(row).toEqual(expected)
        })
    })

    test('marshalling/double-insert-select-roundtrip', async () => {
        await ctx.withRollback(async () => {
            ctx.mockNext(502) // mocked new id; real DB assigns its own
            const newId = await ctx.conn.insertInto(tIssue)
                .values({
                    projectId:      1,
                    number:         9003,
                    title:          'Telemetry spike',
                    status:         'open',
                    priority:       2,
                    estimatedHours: 4.5,
                })
                .returningLastInsertedId()
                .executeInsert()

            expect(ctx.lastSql).toMatchInlineSnapshot(`"insert into issue (project_id, number, title, status, priority, estimated_hours) values ($1, $2, $3, $4, $5, $6) returning id"`)
            expect(ctx.lastParams).toMatchInlineSnapshot(`
              [
                1,
                9003,
                "Telemetry spike",
                "open",
                2,
                4.5,
              ]
            `)

            const expected = { id: newId, hours: 4.5 }
            ctx.mockNext(expected)
            const row = await ctx.conn.selectFrom(tIssue)
                .where(tIssue.number.equals(9003))
                .select({
                    id:    tIssue.id,
                    hours: tIssue.estimatedHours,
                })
                .executeSelectOne()

            expect(ctx.lastSql).toMatchInlineSnapshot(`"select id as id, estimated_hours as hours from issue where number = $1"`)
            expect(ctx.lastParams).toMatchInlineSnapshot(`
              [
                9003,
              ]
            `)
            assertType<Exact<typeof row, { id: number; hours?: number }>>()
            expect(row).toEqual(expected)
        })
    })
})
