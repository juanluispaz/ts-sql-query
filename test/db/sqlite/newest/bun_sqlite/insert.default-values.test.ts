// Behavioral coverage of `connection.insertInto(t).defaultValues()`,
// which triggers `_buildInsertDefaultValues` in each SqlBuilder. Each
// dialect emits a different form: SQLite/PostgreSQL emit
// `INSERT INTO t DEFAULT VALUES`; MySQL/MariaDB emit
// `INSERT INTO t () VALUES ()`; Oracle expands all sequence-backed
// columns explicitly. No existing test exercises this path.

import { afterAll, beforeAll, beforeEach, describe, expect, test } from '../../../../lib/testRunner.js'
import { Table } from '../../../../../src/Table.js'
import { DBConnection } from '../../domain/connection.js'
import { ctx } from './setup.js'

// Local-only table whose only column is an autogenerated PK. On some
// dialects (mysql / oracle / sqlserver) `RequiredColumnsForSetOf` still
// resolves non-empty here, so `.defaultValues()` types as `never` —
// the cast through `any` keeps the runtime path exercised everywhere.
class TDefaultsOnly extends Table<DBConnection, 'TDefaultsOnly'> {
    id = this.autogeneratedPrimaryKey('id', 'int')
    constructor() { super('defaults_only_table_only_for_sql_test') }
}
const tDefaultsOnly = new TDefaultsOnly()

describe(ctx.label, () => {
    beforeAll(() => ctx.up(), ctx.timeoutMs)
    afterAll(() => ctx.down(), ctx.timeoutMs)
    beforeEach(() => { ctx.reset() })

    test('insert default values', async () => {
        ctx.mockNext(99)
        try {
            await (ctx.conn.insertInto(tDefaultsOnly) as any)
                .defaultValues()
                .returningLastInsertedId()
                .executeInsert()
        } catch {
            // expected on real DB — the local table doesn't exist in
            // the seed; the SQL builder still emits the DEFAULT VALUES
            // form that this test checks.
        }
        expect(ctx.lastSql).toMatchInlineSnapshot(`"insert into defaults_only_table_only_for_sql_test default values returning id"`)
        expect(ctx.lastParams).toMatchInlineSnapshot(`[]`)
    })
})
