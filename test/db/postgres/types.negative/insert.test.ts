// Compile-time negative tests for INSERT-shaped APIs on the postgres dialect.
// See `select.test.ts` for the rationale and conventions.

import { test, expect } from '../../../lib/testRunner.js'
import type { DBConnection } from '../domain/connection.js'
import { tIssue, tOrganization, tProject } from '../domain/connection.js'

// `connection` is type-only — the function below is checked by tsc but
// never invoked at runtime, so we don't need a real instance and don't
// open any driver-level resource just to make the file load.
declare const connection: DBConnection

function _typeNegatives() {
    // Rule: optional columns (here `body`) on an INSERT can be omitted, but
    // required columns must be present. The `status` field below is
    // required and missing, which must not compile.
    // @ts-expect-error required column `status` is missing from the INSERT
    void connection.insertInto(tIssue).values({
        projectId: 1,
        number:    1,
        title:     'x',
        priority:  1,
    })

    // Rule: column value types must match. `priority` is `int`; passing a
    // string must not compile. tsgo reports the error at the offending
    // property, not at the values() call site, so the directive sits on
    // the property — see test/WRITING_TESTS.md for why this asymmetry exists.
    void connection.insertInto(tIssue).values({
        projectId: 1,
        number:    1,
        title:     'x',
        status:    'open',
        // @ts-expect-error priority is int, not string
        priority:  'high',
    })

    // Rule: cannot insert columns that don't exist on the table. tsgo
    // reports the excess-property error at the property line itself —
    // directive placement matches.
    void connection.insertInto(tProject).values({
        organizationId:    1,
        name:              'x',
        slug:              'x',
        // @ts-expect-error 'nonExistentColumn' is not a column of tProject
        nonExistentColumn: 'x',
    })

    // Note: autogeneratedPrimaryKey('id', 'int') still accepts an
    // explicit id in insert values — the lib treats autogenerated as
    // "has a default" rather than "cannot be set". The schema-level
    // override holds at the SQL layer; no compile-time guard for now.

    // Rule: PostgreSQL's `ON CONFLICT DO UPDATE` requires an explicit
    // conflict target — either a column list (`onConflictOn(col)`) or
    // a constraint name (`onConflictOnConstraint(name)`). The bare
    // `.onConflictDoUpdateSet({...})` / `.onConflictDoUpdateSetIfValue(...)`
    // / `.onConflictDoUpdateDynamicSet()` forms are not typed on
    // PostgreSqlConnection — calling them must not compile. Covers the
    // .set(), .values()
    // and .from(select) entry chains so a regression in any of the three
    // shapes is caught here.
    const orgSelect = connection.selectFromNoTable().select({
        name: connection.const('x', 'string'),
        plan: connection.const('pro', 'string'),
    })
    // @ts-expect-error bare onConflictDoUpdateSet on PG via .set(...) — needs onConflictOn(col)
    void connection.insertInto(tOrganization).set({ name: 'x', plan: 'pro' }).onConflictDoUpdateSet({ plan: 'enterprise' })
    // @ts-expect-error bare onConflictDoUpdateSet on PG via .values([...]) — needs onConflictOn(col)
    void connection.insertInto(tOrganization).values([{ name: 'x', plan: 'pro' }]).onConflictDoUpdateSet({ plan: 'enterprise' })
    // @ts-expect-error bare onConflictDoUpdateSet on PG via .from(select) — needs onConflictOn(col)
    void connection.insertInto(tOrganization).from(orgSelect).onConflictDoUpdateSet({ plan: 'enterprise' })
    // @ts-expect-error bare onConflictDoUpdateSetIfValue on PG via .set(...) — needs onConflictOn(col)
    void connection.insertInto(tOrganization).set({ name: 'x', plan: 'pro' }).onConflictDoUpdateSetIfValue({ plan: 'enterprise' })
    // @ts-expect-error bare onConflictDoUpdateDynamicSet on PG via .set(...) — needs onConflictOn(col)
    void connection.insertInto(tOrganization).set({ name: 'x', plan: 'pro' }).onConflictDoUpdateDynamicSet()

    // Rule: `onConflictOnConstraint(...)` accepts only a `RawFragment`. A
    // constraint name is a SQL identifier, not a runtime value: binding it
    // as a parameter (`$N`) used to be silently accepted by the type but
    // rejected by real PostgreSQL with `ERROR: syntax error at or near
    // "$N"`. The string and `IStringValueSource` overloads have been
    // removed so the only way to supply a constraint name is via raw SQL
    // assembled from database introspection.
    void connection.insertInto(tOrganization).set({ name: 'x', plan: 'pro' }).onConflictOnConstraint(connection.rawFragment`organization_name_uniq`).doNothing()
    // @ts-expect-error plain string is no longer accepted — use connection.rawFragment`...`
    void connection.insertInto(tOrganization).set({ name: 'x', plan: 'pro' }).onConflictOnConstraint('organization_name_uniq')
    // @ts-expect-error IStringValueSource is no longer accepted — use connection.rawFragment`...`
    void connection.insertInto(tOrganization).set({ name: 'x', plan: 'pro' }).onConflictOnConstraint(connection.const('organization_name_uniq', 'string'))

    // Rule: after a shaped static one-shot `onConflictOn(...).doUpdateSet({renamed})`
    // the returned node stays shaped — a chained `.set(...)` and the `*When` arms
    // accept only the renamed shape keys, never the real columns.
    void connection.insertInto(tProject)
        .shapedAs({ orgId: 'organizationId', projectName: 'name', projectSlug: 'slug' })
        .set({ orgId: 1, projectName: 'a', projectSlug: 'b' })
        .onConflictOn(tProject.organizationId, tProject.slug)
        .doUpdateSet({ projectName: 'one-shot' })
        // @ts-expect-error real column 'name' is not a shaped key on the chained on-conflict set
        .set({ name: 'chained' })
    void connection.insertInto(tProject)
        .shapedAs({ orgId: 'organizationId', projectName: 'name', projectSlug: 'slug' })
        .set({ orgId: 1, projectName: 'a', projectSlug: 'b' })
        .onConflictOn(tProject.organizationId, tProject.slug)
        .doUpdateSet({ projectName: 'one-shot' })
        // @ts-expect-error real column 'name' is not a shaped key on the on-conflict setWhen
        .setWhen(true, { name: 'when' })

}

test('insert-negative-types', () => {
    expect(typeof _typeNegatives).toBe('function')
})
