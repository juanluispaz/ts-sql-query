CREATE OR REPLACE PROCEDURE archive_project(p_id IN NUMBER, p_reason IN VARCHAR2) AS BEGIN UPDATE project SET archived_at = CURRENT_TIMESTAMP, name = name || ' [archived: ' || p_reason || ']' WHERE id = p_id; END;

CREATE OR REPLACE FUNCTION count_open_issues(p_id IN NUMBER) RETURN NUMBER AS v NUMBER; BEGIN SELECT COUNT(*) INTO v FROM issue WHERE project_id = p_id AND status = 'open'; RETURN v; END;

CREATE OR REPLACE FUNCTION project_name(p_id IN NUMBER) RETURN VARCHAR2 AS v VARCHAR2(255); BEGIN SELECT name INTO v FROM project WHERE id = p_id; RETURN v; END;

CREATE OR REPLACE FUNCTION total_view_count(p_id IN NUMBER) RETURN NUMBER AS v NUMBER; BEGIN SELECT COALESCE(SUM(view_count),0) INTO v FROM issue WHERE project_id = p_id; RETURN v; END;

CREATE OR REPLACE FUNCTION latest_issue_at(p_id IN NUMBER) RETURN TIMESTAMP AS v TIMESTAMP; BEGIN SELECT MAX(created_at) INTO v FROM issue WHERE project_id = p_id; RETURN v; END;

CREATE OR REPLACE FUNCTION estimated_total(p_id IN NUMBER) RETURN BINARY_DOUBLE AS v BINARY_DOUBLE; BEGIN SELECT COALESCE(SUM(estimated_hours),0) INTO v FROM issue WHERE project_id = p_id; RETURN v; END;

