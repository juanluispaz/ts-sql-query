// Documentation snippets for the Utility types page
// (docs/advanced/utility-types.md). These are pure compile-time type
// utilities; the test bodies are mostly `assertType<Exact<...>>` so
// regressions in the public type surface are caught immediately.

import { beforeAll, afterAll, beforeEach, describe, test } from '../../../../lib/testRunner.js'
import { assertType, type Exact, type Extends } from '../../../../lib/assertType.js'
import type {
    ColumnKeys, WritableColumnKeys, WritableShapeFor,
    IdColumnKeys, AutogeneratedIdColumnKeys, ProvidedIdColumnKeys,
    InsertableValues, InsertableRow,
    InsertableValuesShapedAs, InsertableRowShapedAs,
    UpdatableValues, UpdatableRow,
    UpdatableValuesShapedAs, UpdatableRowShapedAs,
    SelectedValues, SelectedRow,
    SelectedValuesProjectedAsNullable, SelectedRowProjectedAsNullable,
    UpdatableOnInsertConflictValues, UpdatableOnInsertConflictRow,
    UpdatableOnInsertConflictValuesShapedAs, UpdatableOnInsertConflictRowShapedAs,
} from '../../../../../src/extras/types.js'
import { tAppUser, tCountry, tIssue, tOrganization, tProject, tProjectRelease, vProjectOverview } from '../../domain/connection.js'
import { ctx } from './setup.js'

describe(ctx.label, () => {
    beforeAll(() => ctx.up(), ctx.timeoutMs)
    afterAll(() => ctx.down(), ctx.timeoutMs)
    beforeEach(() => { ctx.reset() })

    test('docs:utility-types/column-keys', () => {
        // doc-start
        type OrgColumns = ColumnKeys<typeof tOrganization>
        // doc-end
        assertType<Exact<OrgColumns, 'id' | 'name' | 'plan' | 'verified' | 'createdAt'>>()
    })

    test('docs:utility-types/writable-column-keys', () => {
        // doc-start
        type OrgWritable = WritableColumnKeys<typeof tOrganization>
        // doc-end
        // All columns of tOrganization are writable (no computed/virtual).
        assertType<Exact<OrgWritable, 'id' | 'name' | 'plan' | 'verified' | 'createdAt'>>()
    })

    test('docs:utility-types/writable-shape', () => {
        // doc-start
        type OrgShape = WritableShapeFor<typeof tOrganization>
        // doc-end
        assertType<Exact<OrgShape, {
            id:        'id'
            name:      'name'
            plan:      'plan'
            verified:  'verified'
            createdAt: 'createdAt'
        }>>()
    })

    test('docs:utility-types/id-column-keys', () => {
        // doc-start
        type OrgId = IdColumnKeys<typeof tOrganization>
        // doc-end
        assertType<Exact<OrgId, 'id'>>()
    })

    test('docs:utility-types/autogenerated-id-keys', () => {
        // doc-start
        type OrgAuto = AutogeneratedIdColumnKeys<typeof tOrganization>
        // doc-end
        // The id is autogenerated via autogeneratedPrimaryKey('id', 'int').
        // It also includes columns with defaults (verified, createdAt) by
        // the current implementation — the type matches
        // `WritableDBColumnWithDefaultValue` not strictly PK.
        assertType<Extends<'id', OrgAuto>>()
        assertType<Extends<OrgAuto, 'id' | 'verified' | 'createdAt'>>()
    })

    test('docs:utility-types/provided-id-keys', () => {
        // doc-start
        type OrgProvided = ProvidedIdColumnKeys<typeof tOrganization>
        // doc-end
        // No non-autogenerated PK columns → never.
        assertType<Exact<OrgProvided, never>>()
    })

    test('docs-extra:utility-types/insertable-values', () => {
        // The insert API requires the writable, non-autogenerated, non-
        // default fields. For tOrganization those are name and plan
        // (id is autogenerated, createdAt has a default).
        type OrgInsert = InsertableValues<typeof tOrganization>
        // Sanity check at least one mandatory field
        assertType<Extends<OrgInsert, { name: string }>>()
    })

    test('docs-extra:utility-types/insertable-row-accepts-value-sources', () => {
        // InsertableRow is the looser variant that also accepts
        // IValueSource expressions (e.g. tProject.organizationId).
        type ProjInsertRow = InsertableRow<typeof tProject>
        // Use Extends in this direction — the type is broader than
        // InsertableValues.
        const probe: ProjInsertRow = {
            organizationId: 1,
            slug:           'demo',
            name:           'demo',
        }
        void probe
    })

    test('docs-extra:utility-types/updatable-values', () => {
        type IssueUpdate = UpdatableValues<typeof tIssue>
        assertType<Extends<{ status: string }, IssueUpdate>>()
    })

    test('docs-extra:utility-types/updatable-row-accepts-value-sources', () => {
        type IssueUpdateRow = UpdatableRow<typeof tIssue>
        // Same logic as InsertableRow — UpdatableRow accepts value
        // sources in addition to literals.
        const probe: IssueUpdateRow = {
            title: 'new title',
        }
        void probe
    })

    test('docs-extra:utility-types/selected-values', () => {
        // SelectedValues mirrors the result type of `selectFrom(t).select(...)`
        // with all columns picked.
        type OrgSelected = SelectedValues<typeof tOrganization>
        // The selected type contains required fields for required cols.
        assertType<Extends<OrgSelected, { id: number; name: string; plan: string }>>()
    })

    test('docs-extra:utility-types/selected-row-is-alias', () => {
        type A = SelectedRow<typeof tOrganization>
        type B = SelectedValues<typeof tOrganization>
        assertType<Exact<A, B>>()
    })

    test('docs:utility-types/updatable-on-insert-conflict-values', () => {
        // Section "Updatable values in case of conflict on insert" — the
        // type of the object accepted by `.onConflictDoUpdateSet({...})`
        // when no value-sources are supplied.
        // doc-start
        type OrgUpsert = UpdatableOnInsertConflictValues<typeof tOrganization>
        // doc-end
        const probe: OrgUpsert = { plan: 'enterprise' }
        void probe
    })

    test('docs:utility-types/updatable-on-insert-conflict-row', () => {
        // Section "Updatable row in case of conflict on insert" — same
        // intent as the *Values type but also accepts SQL value-sources.
        // doc-start
        type OrgUpsertRow = UpdatableOnInsertConflictRow<typeof tOrganization>
        // doc-end
        const probe: OrgUpsertRow = { plan: 'enterprise' }
        void probe
    })

    test('docs-extra:utility-types/selected-values-projected-as-nullable', () => {
        // The …ProjectedAsNullable variants project optional columns as a
        // present `T | null` instead of the optional `?: T` that the plain
        // SelectedValues/SelectedRow emit. tProject.archivedAt is the only
        // optionalColumn; every other column stays required.
        type ProjNullable = SelectedValuesProjectedAsNullable<typeof tProject>
        assertType<Exact<ProjNullable, {
            id:             number
            organizationId: number
            name:           string
            slug:           string
            published:      boolean
            archivedAt:     Date | null
            createdAt:      Date
        }>>()

        // Contrast: the plain SelectedValues keeps archivedAt optional, so the
        // two projections are not the same type.
        type ProjPlain = SelectedValues<typeof tProject>
        assertType<Extends<Exact<ProjNullable, ProjPlain>, false>>()
    })

    test('docs-extra:utility-types/selected-row-projected-as-nullable-is-alias', () => {
        // SelectedRowProjectedAsNullable and SelectedValuesProjectedAsNullable
        // are aliases of the same definition.
        type A = SelectedRowProjectedAsNullable<typeof tProject>
        type B = SelectedValuesProjectedAsNullable<typeof tProject>
        assertType<Exact<A, B>>()
    })

    test('docs:utility-types/insertable-values-shaped-as', () => {
        // doc-start
        const myShape = {
            newEmail:    'email',
            newFullName: 'fullName',
        } as const
        type InsShaped = InsertableValuesShapedAs<typeof tAppUser, typeof myShape>
        // doc-end
        // ResolveShape renames the column keys; both remain mandatory because
        // email/full_name are required, non-default columns.
        const probe: InsShaped = { newEmail: 'a@x.com', newFullName: 'Ada' }
        void probe
    })

    test('docs:utility-types/insertable-row-shaped-as', () => {
        // doc-start
        const myShape = {
            newEmail:    'email',
            newFullName: 'fullName',
        } as const
        type InsRowShaped = InsertableRowShapedAs<typeof tAppUser, typeof myShape>
        // doc-end
        // The *Row* variant additionally accepts value-sources under the
        // renamed keys.
        const literal:  InsRowShaped = { newEmail: 'a@x.com',        newFullName: 'Ada' }
        const valueSrc: InsRowShaped = { newEmail: tAppUser.email,   newFullName: tAppUser.fullName }
        void literal
        void valueSrc
    })

    test('docs:utility-types/updatable-values-shaped-as', () => {
        // doc-start
        const myShape = {
            newEmail:    'email',
            newFullName: 'fullName',
        } as const
        type UpdShaped = UpdatableValuesShapedAs<typeof tAppUser, typeof myShape>
        // doc-end
        // Update is partial — a single renamed key is enough.
        const probe: UpdShaped = { newEmail: 'a@x.com' }
        void probe
    })

    test('docs:utility-types/updatable-row-shaped-as', () => {
        // doc-start
        const myShape = {
            newEmail:    'email',
            newFullName: 'fullName',
        } as const
        type UpdRowShaped = UpdatableRowShapedAs<typeof tAppUser, typeof myShape>
        // doc-end
        // The *Row* variant additionally accepts value-sources under the
        // renamed keys.
        const literal:  UpdRowShaped = { newFullName: 'Ada' }
        const valueSrc: UpdRowShaped = { newFullName: tAppUser.fullName }
        void literal
        void valueSrc
    })

    test('docs:utility-types/updatable-on-insert-conflict-values-shaped-as', () => {
        // doc-start
        const myShape = {
            newEmail:    'email',
            newFullName: 'fullName',
        } as const
        type ConflictValuesShaped = UpdatableOnInsertConflictValuesShapedAs<typeof tAppUser, typeof myShape>
        // doc-end
        const probe: ConflictValuesShaped = { newFullName: 'Ada' }
        void probe
    })

    test('docs-extra:utility-types/updatable-on-insert-conflict-row-shaped-as-equals-values', () => {
        // Both on-conflict shaped aliases delegate to OnConflictUpdateValues,
        // so the Row form is exactly the Values form for this pair.
        const myShape = {
            newEmail:    'email',
            newFullName: 'fullName',
        } as const
        type ConflictRowShaped    = UpdatableOnInsertConflictRowShapedAs<typeof tAppUser, typeof myShape>
        type ConflictValuesShaped = UpdatableOnInsertConflictValuesShapedAs<typeof tAppUser, typeof myShape>
        assertType<Exact<ConflictRowShaped, ConflictValuesShaped>>()
    })

    test('docs-extra:utility-types/column-keys-view-includes-virtual-column', () => {
        // vProjectOverview.nameUpper is a virtualColumnFromFragment — it is an
        // AnyValueSource, so it appears in ColumnKeys alongside the plain
        // columns of the view.
        type ViewColumns = ColumnKeys<typeof vProjectOverview>
        assertType<Exact<ViewColumns,
            'id' | 'organizationId' | 'name' | 'archivedAt'
            | 'organizationName' | 'organizationPlan' | 'nameUpper'>>()
    })

    test('docs-extra:utility-types/writable-column-keys-view-is-never', () => {
        // No view column is a WritableDBColumn (views are not insert/update
        // targets), so WritableColumnKeys collapses to never — including the
        // virtual nameUpper.
        type ViewWritable = WritableColumnKeys<typeof vProjectOverview>
        assertType<Exact<ViewWritable, never>>()
    })

    test('docs-extra:utility-types/provided-primary-key-column-keys', () => {
        // tCountry.code is `primaryKey('code', 'string')` — a CALLER-PROVIDED
        // primary key (no autogeneration). It is an id column AND a
        // provided-id column, but NOT an autogenerated-id column: the
        // non-`never` ProvidedIdColumnKeys branch the autogenerated-PK tables
        // can never surface.
        assertType<Exact<IdColumnKeys<typeof tCountry>, 'code'>>()
        assertType<Exact<ProvidedIdColumnKeys<typeof tCountry>, 'code'>>()
        assertType<Exact<AutogeneratedIdColumnKeys<typeof tCountry>, never>>()
    })

    test('docs-extra:utility-types/provided-primary-key-is-required-on-insert', () => {
        // Because the PK is provided (not autogenerated), it is a MANDATORY
        // field in the insert value shape — unlike the autogenerated `id` of
        // tOrganization, which is omitted from InsertableValues entirely.
        type CountryInsert = InsertableValues<typeof tCountry>
        assertType<Extends<CountryInsert, { code: string }>>()
        const probe: CountryInsert = { code: 'US', name: 'United States', region: 'Americas' }
        void probe
    })

    test('docs-extra:utility-types/computed-and-virtual-columns-excluded-from-writable', () => {
        // tProjectRelease.notes is a `computedColumn` and .versionTag is a
        // `virtualColumnFromFragment`. Both are AnyValueSource, so both appear
        // in ColumnKeys; neither is a WritableDBColumn, so both are excluded
        // from WritableColumnKeys. This isolates the computed/virtual
        // exclusion against a NON-EMPTY writable surface (a view can't — every
        // view column is non-writable, so its WritableColumnKeys is `never`).
        assertType<Exact<ColumnKeys<typeof tProjectRelease>,
            'id' | 'projectId' | 'version' | 'channel' | 'signingKey'
            | 'releasedOn' | 'cutoffTime' | 'signedOffAt' | 'notes' | 'versionTag'>>()
        assertType<Exact<WritableColumnKeys<typeof tProjectRelease>,
            'id' | 'projectId' | 'version' | 'channel' | 'signingKey'
            | 'releasedOn' | 'cutoffTime' | 'signedOffAt'>>()
        // The writable surface is non-empty, so this is a genuine exclusion
        // of `notes`/`versionTag`, not the degenerate view case.
        assertType<Extends<WritableShapeFor<typeof tProjectRelease>, { id: 'id' }>>()
    })
})
